#include "renderer/effects/Effect.hpp"

//-----------------------------------------------------------------------------

namespace jl {

//-----------------------------------------------------------------------------

void Effect::addPass(const Pass & _pass)
{
	m_passes.emplace_back(_pass);
}

//-----------------------------------------------------------------------------

void Effect::apply() const
{
	for (const Pass & pass : m_passes)
	{
		pass.run();
	}
}

//-----------------------------------------------------------------------------

} // namespace jl

